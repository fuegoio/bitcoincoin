<template>
  <v-row>
    <v-col
      v-for="currencyInfo in cryptoCurrenciesWallet"
      :key="currencyInfo.id"
      lg="4"
      md="6"
      sm="12"
    >
      <WalletCurrencyCard :currencyInfo="currencyInfo" />
    </v-col>
  </v-row>
</template>

<script>
import WalletCurrencyCard from '@/components/currencies/WalletCurrencyCard'
import axios from 'axios'
export default {
  name: 'WalletContainer',
  components: { WalletCurrencyCard },
  data: function() {
    return {
      cryptoCurrenciesWallet: [],
    }
  },
  mounted() {
    axios
      .get('http://localhost:8000/api/v1/users/1/wallet')
      .then(response => (this.cryptoCurrenciesWallet = response.data))
  },
}
</script>

